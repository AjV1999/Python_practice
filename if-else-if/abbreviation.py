abbreviation = input("Enter an abbreviation: ")
if abbreviation == "lol":
    print("Laughing out loud")
elif abbreviation == "rofl":
    print("Rolling on the floor laughing")
elif abbreviation == "lmk":
    print("Let me know")
elif abbreviation == "smh":
    print("Shaking my head")
else:
    print("Unknown Abbreviation")